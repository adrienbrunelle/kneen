<p>Who has never wanted to know instantly what his family thinks before making a choice? Or
who never called several friends to have a particular information? But also a professional who
needs to ask a question to a targeted audience. The purpose of this project is to facilitate and
especially make it faster to gather opinion from their contacts or a targeted audience via an
intuitive and responsive clear mobile and web application.</p><p>
The principle is simple: in front of a hesitation or dilemma we question our contacts instantly
to get the issue on the application and can respond easily and without constraint, then the
results are immediately available and we obtain a trend which option to use. The same applies
to a targeted audience that the results are available directly in the interface.</p>
